package team.chess.POJO;

public class Relation {
    private Integer nodeBeginId;

    private Integer nodeEndId;

    private Integer stepId;

    public void setNodeBeginId(Integer nodeBeginId) {
        this.nodeBeginId = nodeBeginId;
    }

    public Integer getNodeBeginId() {
        return nodeBeginId;
    }

    public void setNodeEndId(Integer nodeEndId) {
        this.nodeEndId = nodeEndId;
    }

    public Integer getNodeEndId() {
        return nodeEndId;
    }

    public void setStepId(Integer stepId) {
        this.stepId = stepId;
    }

    public Integer getStepId() {
        return stepId;
    }
}
